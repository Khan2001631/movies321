function MoviesList({ movies, onSetSelectedMovie }) {
  return (
    <ul className="list list-movies">
      {movies?.map((movie) => (
        <Movie
          movie={movie}
          onSetSelectedMovie={onSetSelectedMovie}
          key={movie.imdbID}
        />
      ))}
    </ul>
  );
}

